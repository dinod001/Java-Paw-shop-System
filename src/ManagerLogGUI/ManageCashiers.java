// Method triggered by the "Search" button to find cashier accounts in the system by User ID
    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        if (txtuserID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "User ID can't be empty");
        } else {
            int UserID = Integer.valueOf(txtuserID.getText());
            loginList = logindb.Search(UserID);
            TableModel.setRowCount(0);
            for (Login logDetails : loginList) {
                int ID = logDetails.getUserID();
                String Username = logDetails.getUsername();
                String Password = logDetails.getPassword();
                TableModel.addRow(new Object[]{ID, Username, Password});
            }
        }
    }//GEN-LAST:event_btnsearchActionPerformed
