// Method triggered by the "Delete" button to remove a cashier account from the system
    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        String userID = JOptionPane.showInputDialog(null, "Enter User ID");
        if (userID == null || userID.trim().isEmpty()) {
            return;  // Exit the method to close the dialog
        }
        int UserID = Integer.parseInt(userID);
        if (logindb.delete(UserID)) {
            JOptionPane.showMessageDialog(null, "Deleted successfully");
            viewLoginDetails();  // Refresh or view details
        } else {
            JOptionPane.showMessageDialog(null, "User ID not found");
        }
    }//GEN-LAST:event_btndeleteActionPerformed
// Method triggered by the "Update" button to modify an existing cashier account in the system
