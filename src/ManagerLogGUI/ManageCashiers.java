 // Method to check if the user ID or username is already in use
    public boolean checkUserID() {
        try {
            BufferedReader br = new BufferedReader(new FileReader("UserDetails.txt"));
            String line;
            // Read the file line by line to check for duplicate user IDs or usernames
            try {
                while ((line = br.readLine()) != null) {
                    String extractID = line.split(":")[0];
                    String extractUsername = line.split(":")[1];
                    // Check if the entered user ID matches an existing ID
                    if (extractID.equals(txtuserID.getText())) {
                        JOptionPane.showMessageDialog(null, "This user ID already stored");
                        return false;
                    }
                    // Check if the entered username matches an existing username
                    if (extractUsername.equals(txtUsername.getText())) {
                        JOptionPane.showMessageDialog(null, "This username already stored");
                        return false;
                    }
                }
            } catch (IOException ex) {
                Logger.getLogger(ManageCashiers.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ManageCashiers.class.getName()).log(Level.SEVERE, null, ex);
        }
        return true;
    }
// Method to verify if any required text fields are empty
    public boolean checkBlank() {
        if (txtuserID.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "User ID can't be zero");
            return false;
        }
        if (txtUsername.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "UserName can't be blank");
            return false;
        }
        if (txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Password can't be blank");
            return false;
        }
        return true;
    }
// Method to check if the username and password meet system requirements
    public boolean checkconditions() {
        // Check if the username has at least 4 characters
        if (txtUsername.getText().length() < 4) {
            JOptionPane.showMessageDialog(null, "Your username should have 4 character");
            return false;
        }
        // Validate password requirements: at least 6 characters, includes '@' or '#', and contains at least one digit
        if (txtPassword.getText().length() < 6 || !(txtPassword.toString().contains("@") || txtPassword.toString().contains("#")) || !txtPassword.getText().matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(null, "password should have @ or # with 6 characters including at lest one digit");
            return false;
        }
        return true;
    }
// Method to display all cashier login details from the database in a table format
    public void viewLoginDetails() {
        loginList = logindb.getAll();
        TableModel.setRowCount(0);
        for (Login logindetails : loginList) {
            int UserID = Integer.valueOf(logindetails.getUserID());
            String Username = logindetails.getUsername();
            String Password = logindetails.getPassword();
            TableModel.addRow(new Object[]{UserID, Username, Password});
        }
    }

